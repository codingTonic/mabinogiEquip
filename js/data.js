export const DEFAULT_RUNE_MESSAGE = '장비를 선택하면 사용 가능한 룬이 표시됩니다.';

export function getAllowedRunesForJob(job) {
  const jobRunes = accessoryRunesByJob[job];
  if (jobRunes && jobRunes.length > 0) {
    return [baseRunes[0], ...jobRunes];
  }
  return weaponRunes;
}

export function getRuneDetails(name) {
  return runeCatalog.get(name);
}

function registerRunes(list) {
  list.forEach((rune) => {
    if (!runeCatalog.has(rune.name)) {
      runeCatalog.set(rune.name, rune);
    }
  });
}
